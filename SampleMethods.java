class SampleMethods
{
	
	public static void main(String args[])
	{
		add();
		SampleMethods.multiple();
	}
	
	
	public static void add()
	{
		int a=20;
		int b=40;
		int c=a+b;
		System.out.println("Sum= "+c);
	}
	
	public static void multiple()
	{
		int a=3;
		int b=40;
		int c=a*b;
		System.out.println("Product= "+c);
	}
	
	
	
}